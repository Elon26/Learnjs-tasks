const age2 = 92;

if (age2 < 14 || age2 > 90) {
    alert(`Число ${age2} не в диапазоне от 14 до 90`)
} else {
    alert(`Число ${age2} в диапазоне от 14 до 90`)
}

if (!(age2 >= 14 && age2 <= 90)) {
    alert(`Число ${age2} не в диапазоне от 14 до 90`)
} else {
    alert(`Число ${age2} в диапазоне от 14 до 90`)
}